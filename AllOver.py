p,q,r = 5,15,25

# Arithmetic Operator
print("\n 5 + 15 = ",p+q)

print("\n 25 - 15 = ",r-q)

print("\n 5 * 15 = ",p*q)

print("\n 25 / 5 = ",r/q)

# Comparison Operator
x,y = 11,22
print("\n x < y",x<y)

print("\n x > y",x>y)

print("\n x <= y",x<=y)

print("\n x >= y",x>=y)

print("\n x == y",x==y)

print("\n x != y",x!=y)

